-- Phase 7: AI evaluation suite

CREATE TABLE eval_test_cases (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  job_description_text text NOT NULL,
  expected_keywords jsonb NOT NULL,
  expected_missing_skills jsonb NOT NULL,
  expected_match_range_min int NOT NULL,
  expected_match_range_max int NOT NULL,
  created_at timestamptz DEFAULT now()
);

CREATE TABLE eval_run_results (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  eval_test_case_id uuid REFERENCES eval_test_cases(id) ON DELETE CASCADE,
  run_timestamp timestamptz DEFAULT now(),
  actual_match_score int NOT NULL,
  keyword_precision float NOT NULL,
  keyword_recall float NOT NULL,
  passed boolean NOT NULL,
  notes text
);

CREATE INDEX eval_run_results_case_idx ON eval_run_results(eval_test_case_id);
CREATE INDEX eval_run_results_timestamp_idx ON eval_run_results(run_timestamp DESC);

GRANT SELECT ON TABLE eval_test_cases TO authenticated;
GRANT SELECT ON TABLE eval_run_results TO authenticated;

